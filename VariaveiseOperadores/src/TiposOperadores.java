public class TiposOperadores {
    public static void main(String[] args) {
        // Operadores em Java

        // 1. Operadores Aritméticos
        int soma = 10 + 5; // Adição (+)
        int subtracao = 10 - 5; // Subtração (-)
        int multiplicacao = 10 * 5; // Multiplicação (*)
        int divisao = 10 / 5; // Divisão (/)
        int modulo = 10 % 3; // Módulo (resto da divisão) (%)

        // 2. Operadores de Atribuição
        int numero = 10; // Atribuição (=)
        numero += 5; // Adição e atribuição (+=)
        numero -= 5; // Subtração e atribuição (-=)
        numero *= 5; // Multiplicação e atribuição (*=)
        numero /= 5; // Divisão e atribuição (/=)
        numero %= 3; // Módulo e atribuição (%=)

        // 3. Operadores Relacionais
        boolean maiorQue = 10 > 5; // Maior que (>)
        boolean menorQue = 10 < 5; // Menor que (<)
        boolean maiorOuIgual = 10 >= 5; // Maior ou igual (>=)
        boolean menorOuIgual = 10 <= 5; // Menor ou igual (<=)
        boolean igual = 10 == 5; // Igual a (==)
        boolean diferente = 10 != 5; // Diferente de (!=)

        // 4. Operadores Lógicos
        boolean e = (10 > 5) && (5 < 10); // E lógico (&&)
        boolean ou = (10 > 5) || (5 > 10); // Ou lógico (||)
        boolean nao = !(10 > 5); // Negação lógica (!)

        // 5. Operador de Incremento e Decremento
        int incremento = 10;
        incremento++; // Incremento (++)
        incremento--; // Decremento (--)

         
    }
}